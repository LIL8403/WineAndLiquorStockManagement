    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String Name = txtName.getText();
        String Mobile = txtMobile.getText();
        String Email = txtEmail.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "Fill all required fields!");
        } else {
            try {
                try {
                    Connection con = ConnectionProvider.getCon();
                    PreparedStatement ps = con.prepareStatement("UPDATE customer SET Name=?,Mobile=?,Email=? where ID=?");
                    ps.setString(1, Name);
                    ps.setString(2, Mobile);
                    ps.setString(3, Email);
                    ps.setInt(4, customerPk);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "customer info updated succesfully");
                    setVisible(false);
                    new ManageCustomer().setVisible(true);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

