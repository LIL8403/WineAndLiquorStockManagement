    private void customerTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTabMouseClicked
        // TODO add your handling code here:
        int index = customerTab.getSelectedRow();
        TableModel model = customerTab.getModel();
        
        String ID = model.getValueAt(index, 0).toString();
        customerPk = Integer.parseInt(ID);
        String Name = model.getValueAt(index, 1).toString();
        txtName.setText(Name);
        String Mobile = model.getValueAt(index, 2).toString();
        txtMobile.setText(Mobile);
        String Email = model.getValueAt(index, 3).toString();
        txtEmail.setText(Email);
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_customerTabMouseClicked

