    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) customerTab.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from customer");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("ID"),rs.getString("Name"),rs.getString("Mobile"),rs.getString("Email")});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String Name = txtName.getText();
        String Mobile = txtMobile.getText();
        String Email = txtEmail.getText();
        
        if(validateFields()){
            JOptionPane.showMessageDialog(null, "Fill all required fields!");
        }else {
            try{
                try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("INSERT INTO customer(Name,Mobile,Email) VALUES(?,?,?)");
                ps.setString(1, Name);
                ps.setString(2, Mobile);
                ps.setString(3, Email);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "customer added succesfully");
                setVisible(false);
                new ManageCustomer().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
            
    }//GEN-LAST:event_btnAddActionPerformed

