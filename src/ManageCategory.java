    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = catTab.getSelectedRow();
        TableModel model = catTab.getModel();
        
        try {
                JOptionPane.showConfirmDialog(null, "Do you want to delete this category ?", "Select", JOptionPane.YES_NO_OPTION);
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("DELETE FROM category where CategoryID=?");
                
                ps.setInt(1, CategoryID);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Category Deleted succesfully");
                setVisible(false);
                new ManageCategory().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void catTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_catTabMouseClicked
        // TODO add your handling code here:
        int index = catTab.getSelectedRow();
        TableModel model = catTab.getModel();

        String id = model.getValueAt(index, 0).toString();
        CategoryID = Integer.parseInt(id);
        
        String CategoryName = model.getValueAt(index, 1).toString();
        txtName.setText(CategoryName);
        String Description = model.getValueAt(index, 2).toString();
        txtDesc.setText(Description);
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);

    }//GEN-LAST:event_catTabMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String CategoryName = txtName.getText();
        String Description = txtDesc.getText();
         if ( txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "CategoryName is required!");
        } else {
             try{
                 Connection con = ConnectionProvider.getCon();
                 PreparedStatement ps = con.prepareStatement("UPDATE category SET CategoryName=?,Description=? where CategoryID=?");
                 ps.setString(1, CategoryName);
                 ps.setString(2, Description);
                 ps.setInt(3, CategoryID);
                 ps.executeUpdate();
                 JOptionPane.showMessageDialog(null, "Category updated succesfully");
                 setVisible(false);
                 new ManageCategory().setVisible(true);
             }
             catch(Exception e){
                 JOptionPane.showMessageDialog(null, e);
             }
         }
         btnAdd.setEnabled(false);
    }//GEN-LAST:event_btnUpdateActionPerformed

