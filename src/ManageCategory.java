    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) catTab.getModel();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st =con.createStatement();
            ResultSet rs = st.executeQuery("Select * from category");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("CategoryID"),rs.getString("CategoryName"),rs.getString("Description")});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String CategoryName = txtName.getText();
        String Description = txtDesc.getText();
         if ( txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "CategoryName is required!");
        } else {
             try{
                 Connection con = ConnectionProvider.getCon();
                 PreparedStatement ps = con.prepareStatement("INSERT INTO category(CategoryName,Description) VALUES(?,?)");
                 ps.setString(1, CategoryName);
                 ps.setString(2, Description);
                 ps.executeUpdate();
                 JOptionPane.showMessageDialog(null, "Category created succesfully");
                 setVisible(false);
                 new ManageCategory().setVisible(true);
             }
             catch(Exception e){
                 JOptionPane.showMessageDialog(null, e);
             }
         }
    }//GEN-LAST:event_btnAddActionPerformed

